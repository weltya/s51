var Project = window.Project || {};

(function SessionScopeWrapper($) {
  var authToken;
  Project.authToken
    .then(function setAuthToken(token) {
      if (token) {
        authToken = token;
      } else {
        window.location.href = "/index.html";
      }
    })
    .catch(function handleTokenError(error) {
      alert(error);
      window.location.href = "/index.html";
    });
})(jQuery);
